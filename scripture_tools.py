def detect_scripture_theme(entries):
    """Analyze journal entries and determine the best scripture theme.
    entries: list of journal entry dicts with 'text' and 'mood_score'
    Returns: theme string
    """
    if not entries:
        return "general"

    # Combine all text
    combined_text = " ".join(e.get("text", "") for e in entries).lower()

    # Average mood
    scores = [e.get("mood_score", 3) for e in entries]
    avg_mood = sum(scores) / len(scores) if scores else 3

    # Score each theme by keyword matches
    theme_scores = {}
    for theme, keywords in THEME_KEYWORDS.items():
        score = sum(1 for kw in keywords if kw in combined_text)
        theme_scores[theme] = score

    # Get highest scoring theme
    best_theme = max(theme_scores, key=theme_scores.get)
    best_score = theme_scores[best_theme]

    # If no keywords matched, use mood to determine theme
    if best_score == 0:
        if avg_mood <= 1.5:
            return "comfort"
        elif avg_mood <= 2.5:
            return "anxiety"
        elif avg_mood >= 4.5:
            return "gratitude"
        elif avg_mood >= 3.5:
            return "strength"
        else:
            return "general"

    return best_theme


def get_scripture_for_theme(theme):
    """Get a random scripture for a given theme."""
    import random
    scriptures = SCRIPTURES.get(theme, SCRIPTURES["general"])
    return random.choice(scriptures)
